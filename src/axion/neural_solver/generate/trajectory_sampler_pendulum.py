from examples.double_pendulum.pendulum_articulation_definition import PENDULUM_HEIGHT, LINK_LENGTH



MAX_ANGLE_WITH_Z_AXIS_RAD = 0.64
MAX_D_COEFFICIENT_OFFSET_M = 2.5

"""
Trajectory-mode dataset generator for Pendulum env.
In Pendulum, we use abstract mode for contact sampling, where we directly sample 
contact information instead of using the collision detection in the env.
The contact information is generated by randomly sampling a ground plane.
The ground plane is randomized for each trajectory and keeps fixed during the trajectory.
"""
class TrajectorySamplerPendulum(TrajectorySampler):
    """Data generator that samples random trajectories [(states, controls, next_states)]."""

    def __init__(
        self,
        env: NnTrainingInterface,
        joint_q_min: Union[float, np.ndarray],
        joint_q_max: Union[float, np.ndarray],
        joint_qd_min: Union[float, np.ndarray],
        joint_qd_max: Union[float, np.ndarray],
        joint_act_scale: float,
        contact_prob: float = 0.,
        sampler=UniformSampler()
    ):
        super().__init__(
            env, 
            joint_q_min, joint_q_max, 
            joint_qd_min, joint_qd_max,
            joint_act_scale,
            contact_prob,
            sampler
        )

    def compute_pendulum_points(self, initial_states):
        q0 = torch.pi / 2 - initial_states[..., 0] 
        q1 = q0 - initial_states[..., 1]

        # end of first link:
        x0 = LINK_LENGTH*torch.sin(q0)
        z0 = -LINK_LENGTH*torch.cos(q0) + PENDULUM_HEIGHT
        y0 = torch.zeros_like(x0)
        # end of second link
        x1 = LINK_LENGTH*torch.sin(q1) + x0
        z1 = -LINK_LENGTH*torch.cos(q1) + z0 
        y1 = torch.zeros_like(x1)
        # pendulum pivot (world frame)
        xPivot = torch.zeros_like(x0)
        yPivot = torch.zeros_like(x0)
        zPivot = PENDULUM_HEIGHT * torch.ones_like(x0)

        pivot_xyz = torch.stack([xPivot, yPivot, zPivot], dim=-1)
        link1_xyz = torch.stack([x0, y0, z0], dim=-1)
        link2_xyz = torch.stack([x1, y1, z1], dim=-1)
        return torch.stack([pivot_xyz, link1_xyz, link2_xyz], dim=1)

    def calculate_d_coefficients_from_normals(self, 
        plane_normals: torch.Tensor, 
        normalized_plane_normals: torch.Tensor):
        """
        Calculate baseline d coefficients for the contact planes such that the plane would
        touch the sphere circumscribed by the pendulum
        Arg:
            plane_normals before normalization (num_envs, 3)
            normalized_plane_normals (num_envs, 3)
        Ret:
            d coefficents of the tangent planes (num_envs, 1)
        """
        a = normalized_plane_normals[:, 0]
        b = normalized_plane_normals[:, 1]
        c = normalized_plane_normals[:, 2]
        # Contact point coords:
        x = plane_normals[:, 0]
        y = plane_normals[:, 1]
        # Calculation:
        z = plane_normals[:, 2] + PENDULUM_HEIGHT
        d = -a*x -b*y - c*z
        return d.unsqueeze(1) # make it a 2d tensor (nums_ens, 1)

    def check_for_pendulum_crossing_plane(
        self,
        pendulum_points: torch.Tensor,
        plane_normals: torch.Tensor,
        plane_d_coefficients: torch.Tensor,
    ) -> None:
        """
        For each world, ensure all pendulum points lie on one side of the plane (n·x + d = 0).
        Where the pendulum would cross the plane, set plane_normals to (0, 0, 1) and d to 0 in-place.
        """
        # signed distances per point: (num_worlds, 3) for plane n·x + d = 0
        print(f"a b c: {plane_normals}, d: {plane_d_coefficients}")
        print(f"pendulum_points {pendulum_points}")
        signed_dists = (
            (pendulum_points * plane_normals.unsqueeze(1)).sum(dim=-1)
            + plane_d_coefficients
        )
        # plane_normals_ext = plane_normals.unsqueeze(-1).repeat(1, 1, 3)
        # signed_dists = (pendulum_points* plane_normals_ext).sum(dim=-1)
        # signed_dists += plane_d_coefficients
        print(f"signed distances {signed_dists}")
        min_d = signed_dists.min(dim=1).values
        max_d = signed_dists.max(dim=1).values
        crossing = (min_d < 0) & (max_d > 0)
        num_crossing = crossing.sum().item()
        if num_crossing > 0:
            num_total = plane_normals.shape[0]
            crossing_indices = crossing.nonzero(as_tuple=True)[0].tolist()
            print(f"Plane normals edited (pendulum crossing plane): {num_crossing} / {num_total} at indices {crossing_indices}")
            plane_normals[crossing] = torch.tensor(
                [0.0, 0.0, 1.0],
                dtype=plane_normals.dtype,
                device=plane_normals.device,
            )
            plane_d_coefficients[crossing] = 0.0
        else:
            print("no plane normals were edited")

    def sample_trajectories(
        self, 
        num_transitions: int, 
        trajectory_length: int, 
        passive: bool = False,
        render: bool = False,
        export_video: bool = False,
        export_video_path: str = None
    ) -> List[Dict]:
        """
        States + Contact data
        """

        rollout_batches = {
            'gravity_dir': [],
            'root_body_q': [],
            'states': [],
            'contacts': {
                'contact_normals': [],
                'contact_depths': [],
                'contact_points_0': [],
                'contact_points_1': [],
                'contact_thicknesses': []
            },
            'joint_acts': [],
            'next_states': [],
            'plane_coefficients': []
        }
        
        progress = trange(
            0, num_transitions, 
            self.num_envs * trajectory_length, 
            desc="Sampling state transitions"
        )
        
        # allocate round-wise buffers
        initial_states = torch.empty(
            self.num_envs,
            self.state_dim,
            dtype=torch.float32,
            device=self.torch_device)
        states = torch.empty(
            trajectory_length, 
            self.num_envs,
            self.state_dim,
            dtype=torch.float32,
            device=self.torch_device)
        next_states = torch.empty(
            trajectory_length, 
            self.num_envs,
            self.state_dim,
            dtype=torch.float32,
            device=self.torch_device)
        joint_acts = torch.empty(
            trajectory_length, 
            self.num_envs,
            self.joint_act_dim,
            dtype=torch.float32,
            device=self.torch_device)
        root_body_q = torch.empty(
            trajectory_length,
            self.num_envs,
            7,
            dtype=torch.float32,
            device=self.torch_device)
        gravity_dir = torch.empty(
            trajectory_length, 
            self.num_envs,
            3,
            dtype=torch.float32,
            device=self.torch_device)
        gravity_dir[:, :, self.env.model.up_axis] = -1.0

        num_contacts_per_env = self.env.abstract_contacts.num_contacts_per_env
        
        contact_normals = torch.empty(
            trajectory_length,
            self.num_envs,
            num_contacts_per_env,
            3,
            dtype=torch.float32,
            device=self.torch_device
        )
        
        contact_depths = torch.empty(
            trajectory_length,
            self.num_envs,
            num_contacts_per_env,
            dtype=torch.float32,
            device=self.torch_device
        )
        contact_points_0 = torch.empty(
            trajectory_length, 
            self.num_envs,
            num_contacts_per_env,
            3,
            dtype=torch.float32,
            device=self.torch_device
        )
        contact_points_1 = torch.empty(
            trajectory_length, 
            self.num_envs,
            num_contacts_per_env,
            3,
            dtype=torch.float32,
            device=self.torch_device
        )
        contact_thicknesses = torch.empty(
            trajectory_length,
            self.num_envs,
            num_contacts_per_env,
            dtype=torch.float32,
            device=self.torch_device
        )
        plane_normals = torch.empty(
            self.num_envs,
            3,
            dtype = torch.float32,
            device = self.torch_device
        )
        plane_d_offsets = torch.zeros(
            self.num_envs,
            1,
            dtype = torch.float32,
            device = self.torch_device
        )

        self.env.set_env_mode('ground-truth')
        
        _eval_collisions = self.env.eval_collisions
        self.env.set_eval_collisions(True)

        if export_video:
            self.env.start_video_export(export_video_path)

        rounds = 0
        for _ in progress:
            rounds += 1

            # generate random initial states
            self.sampler.sample(
                batch_size = self.num_envs, 
                low = self.states_min,
                high = self.states_max,
                data = initial_states)

            pendulum_points_world = self.compute_pendulum_points(initial_states)
        
           # generate random joint_acts
            self.sampler.sample(
                batch_size = self.num_envs * trajectory_length, 
                low = -self.joint_act_scale,
                high = self.joint_act_scale,
                data = joint_acts.view(-1, self.joint_act_dim)
            )
            # ground plane normals (batched per env), at most 0.64 rad from world z
            self.sampler.sample_plane_normals(
                batch_size=self.num_envs,
                data=plane_normals,
            ) 

            # Normalize the normals
            normalized_plane_normals = torch.nn.functional.normalize(plane_normals, p = 2.0, dim = -1)
            print(f"Plane normals shape: {plane_normals.shape}")
            print(f"Plane normals normalized shape: {normalized_plane_normals.shape}")

            plane_d_coefficients = self.calculate_d_coefficients_from_normals(plane_normals, normalized_plane_normals)

            self.sampler.sample_plane_d_coefficient_offsets(
                batch_size=self.num_envs,
                max_d_offset= MAX_D_COEFFICIENT_OFFSET_M,
                data=plane_d_offsets,
            )

            # Finalize the d coefficients
            plane_d_coefficients = plane_d_coefficients + plane_d_offsets # minus because we want to push the plane IN the sphere 
            # Check if the sampled plane does not cross the pendulum
            
            self.check_for_pendulum_crossing_plane(
                pendulum_points_world, normalized_plane_normals, plane_d_coefficients
            )

            # Build (a,b,c,d) per round: (T, num_envs, 4)
            # Negate both normal and d so the stored plane equation stays consistent:
            # n·x + d = 0  <=>  (-n)·x + (-d) = 0
            flipped_normals = -normalized_plane_normals
            flipped_d = -plane_d_coefficients
            plane_normals_expanded = flipped_normals.unsqueeze(0).expand(trajectory_length, -1, -1)
            plane_d_expanded = flipped_d.unsqueeze(0).expand(trajectory_length, -1, -1)
            plane_coefficients = torch.cat([plane_normals_expanded, plane_d_expanded], dim=-1)

            if passive:
                joint_acts *= 0.

            # set initial states and scene (plane n·x + d = 0)
            self.env.reset(
                initial_states=initial_states,
                plane_normals= flipped_normals,
                plane_d_coefficients= flipped_d,
            )

            for step in range(trajectory_length):
                
                root_body_q[step, :, :].copy_(self.env.root_body_q)
                states[step, :, :].copy_(self.env.states)
                next_states[step, :, :].copy_(
                    self.env.step_with_joint_act(
                        joint_acts[step, :, :],
                        env_mode = 'ground-truth'
                    )
                )
                converted_gravity = self.env.get_gravity_dir()
                gravity_dir[step,:,:].copy_(converted_gravity)
                converted_contacts = self.env.convert_newton_contacts_to_contacts_for_nn_model()
                contact_normals[step, :, :].copy_(converted_contacts["contact_normals"])
                contact_depths[step, :, :].copy_(converted_contacts["contact_depths"])
                contact_points_0[step, :, :].copy_(converted_contacts["contact_points_0"])
                contact_points_1[step, :, :].copy_(converted_contacts["contact_points_1"])
                contact_thicknesses[step, :, :].copy_(converted_contacts["contact_thicknesses"])

            # save to trajectories
            rollout_batches['gravity_dir'].append(gravity_dir.clone())
            rollout_batches['root_body_q'].append(root_body_q.clone())
            rollout_batches['states'].append(states.clone())
            rollout_batches['contacts']['contact_normals'].append(contact_normals.clone())
            rollout_batches['contacts']['contact_depths'].append(contact_depths.clone())
            rollout_batches['contacts']['contact_points_0'].append(contact_points_0.clone())
            rollout_batches['contacts']['contact_points_1'].append(contact_points_1.clone())
            rollout_batches['contacts']['contact_thicknesses'].append(contact_thicknesses.clone())
            rollout_batches['joint_acts'].append(joint_acts.clone())
            rollout_batches['next_states'].append(next_states.clone())
            #plane_normals_expanded = plane_normals.unsqueeze(0).expand(trajectory_length, -1, -1)
            #d = torch.zeros(plane_normals_expanded.shape[0], plane_normals_expanded.shape[1], 1, dtype = torch.float32, device = self.torch_device)
            #plane_coefficients = torch.cat([plane_normals_expanded, d], dim=-1)
            rollout_batches['plane_coefficients'].append(plane_coefficients.clone())

        print(f'\n\nTotal number of transitions generated: {rounds * self.num_envs * trajectory_length}')

        # merge rollout batches
        rollouts = {}
        for key in rollout_batches:
            if isinstance(rollout_batches[key], dict):
                rollouts[key] = {}
                for sub_key in rollout_batches[key]:
                    rollouts[key][sub_key] = torch.cat(rollout_batches[key][sub_key], dim = 1)
            else:
                rollouts[key] = torch.cat(rollout_batches[key], dim = 1)

        print('[DEBUG] sum(next_states) = ', rollouts['next_states'].sum())
        
        self.env.set_eval_collisions(_eval_collisions)
        
        if export_video:
            self.env.end_video_export()

        return rollouts